simulate_dice <- function(num_rolls) {
  dice1 <- c(1, 2, 3, 4, 5, 6)
  dice2 <- c(1, 2, 3, 4, 5, 6,)

  rolls <- data.frame(Dice1 = integer(num_rolls),Dice2 = integer(num_rolls))

  for (i in 1:num_rolls) {
    rolls[i, "Dice1"] <- sample(dice1, 1)
    rolls[i, "Dice2"] <- sample(dice2, 1)
  }
}
